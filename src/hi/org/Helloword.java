package hi.org;

public class Helloword {

	public static void main(String[] args) {
		// 메인 메소드 에서 프로그램 작성
		// 한줄 주석 : 프로그램에서 처리되지 않는 블락, 설명
		/*
		   여러줄 주석
		 */
		System.out.println("Hellow Word ~!!!!");
		
		// 출력 gn 라인을 개행하라
		System.out.println("두번째 출력 구문 ~~~");
		// syso + <ctrl = spacebar>
		
		
		// 출력하라	( Enter가 적용되지 않는다. )
		System.out.print("안녕");
		System.out.print("하세요");
		
		// 역슬레쉬 n : Enter
		System.out.print("안녕 \n");
		System.out.print("하세요");


	}

}
